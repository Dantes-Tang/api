package com.capgemini.drms.model;

public class EmailLangs {
	  String langSkill;
	  String wriVer;
	  String level;
	  String comment;
	  public String getLangSkill() {
		return langSkill;
	}
	  public void setLangSkill(String langSkill) {
		this.langSkill = langSkill;
	}
	  public String getWriVer() {
		return wriVer;
	}
	  public void setWriVer(String wriVer) {
		this.wriVer = wriVer;
	}
	  public String getLevel() {
		return level;
	}
	  public void setLevel(String level) {
		this.level = level;
	}
	  public String getComment() {
		return comment;
	}
	  public void setComment(String comment) {
		this.comment = comment;
	}
}
